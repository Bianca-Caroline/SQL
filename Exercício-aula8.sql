CREATE DATABASE Escola;

USE Escola;
CREATE TABLE Curso(
	CODCurso CHAR(3) primary key not null,
	Nome VARCHAR(20) not null,
	Mensalidade NUMERIC(10,2)
);


CREATE TABLE Aluno(
	RA CHAR(9) NOT NULL,
	RG CHAR(9),
	Nome VARCHAR(30),
    PRIMARY KEY (RA),
    CODCurso char(3), 
    FOREIGN KEY (CODCurso) REFERENCES Curso (CODCurso)
);


CREATE TABLE Disciplina(
	CODDisc INT(11) primary key NOT NULL,
    Nome CHAR(30),
	NroCreditos INT(11),
    CODCurso CHAR(3) NOT NULL,
	FOREIGN KEY (CODCurso) REFERENCES Curso (CODCurso)
);


CREATE TABLE Boletim(
	RA CHAR(9) PRIMARY KEY NOT NULL,
    CODDisc INT(11) NOT NULL,
	Nota DECIMAL(10,2),
	FOREIGN KEY (RA) REFERENCES Aluno (RA),
    FOREIGN KEY (CODDisc) REFERENCES Disciplina (CODDisc)
);

